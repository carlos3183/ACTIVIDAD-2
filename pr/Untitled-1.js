let numero = 6; //variable modificable

if (numero === 1) {
    console.log("lunes");
} else if (numero === 2) {
    console.log("martes");
} else if (numero === 3) {
    console.log("miércoles");
} else if (numero === 4) {
    console.log("jueves");
} else if (numero === 5) {
    console.log("viernes");
} else if (numero === 6) {
    console.log("sábado");
} else if (numero === 7) {
    console.log("domingo");
} else {
    console.log("Número inválido. Debe ser un número del 1 al 7.");
}